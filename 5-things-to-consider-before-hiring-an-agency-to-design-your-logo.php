        <h3>Hiring someone to design your logo is a great choice. Hiring someone locally is an even better choice!</h3><br>
            
            <!-- featured article -->
        <p><span class="dropcap">A</span> logo is defined in the dictionary as “a symbol or other design adopted by an organization to identify its products, uniform, vehicles, etc.” A logo is important to an organization because it defines them. It separates them from everyone else and expresses who they are at the same time. Logos aren’t as easy as some may think. You want it to be creative and eye-catching because it needs to represent your brand and it’s also going to be on everything you do. So you have the ideas just don’t know how to exactly design it? That’s totally okay! Hire someone. But there are a few things you should consider before hiring an agency to design your logo. </p><br>
        
         <img src="img/blog/5-things-to-consider-before-hiring.jpeg" width="100%"><br><br>

<p><b>1. Make sure there is a domain name to match the name of your company.</b><br> This is for consistency purposes. To ensure that you can be found and be separated from everyone else.
</p><br>

<p><b>2. Do your research.</b> It is okay to look at other company’s logos. It will help you see what separates them from others. Researching will give you a better understand of what a logo entitles and can assist you in deciding what you want for your logo.
</p><br>

<p><b>3. Decide on the type of logo you want. </b> Do you want a brand mark (icon or symbol)? A combination mark (text and symbol)? Or maybe you want an emblem with text inside the symbol or just your initials as a lettermark? You should know this before hiring someone to design your logo. You want to give them something to work with.</p><br>

<p><b>4. Stay away from cheap logos. </b> Most people try to pay cheap for a logo online but find out the hard way that the quality isn’t good and now they are paying again to have another logo designed. It is better to choose a local professional agency, especially one who’s work you have already seen. 
</p><br>

<p><b>5. Making your decision.</b> Once you choose the agency to design your logo, make sure your information is clear. Communication is the key! Let them know what you expect of them. Also, understand the more complex the logo, the more money you are going to spend.
</p><br>
       
<p>Hiring someone to design your logo is a great choice. Hiring someone locally is an even better choice! You want someone you can sit down and talk with. You want to ask questions about their process and what they expect of you just to ensure that you are comfortable. Remember, a logo defines you and it will be on everything that you do. You want to get it right!
</p> 
