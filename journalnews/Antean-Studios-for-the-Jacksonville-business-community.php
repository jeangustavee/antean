        <p><span class="dropcap">A</span>t Antean Studios, we do believe in making money to stay in business – we also believe all businesses are. Like other businesses, we advertise, have a sales team, and we have clients and employees. This is the essence of running a prim business. The forthright connotation of a business is an organizational entity involved in the provision of goods and services to consumers. It’s just to go back to the fact that being in business has the number one objective: to make money. On the flip side, as a business owner, my theory is to basically sell more, save more and spend less.
</p><br>
        <p>However, Antean Studios is taking another route to its approaches. Most businesses are comfortable with the recurring payment scheme where the profits only go one way; but, we are far away from that. The upshots are that sometimes the clients don’t stay in business for too long. Then the said business has to hunt new clients. Here comes Antean Studios. We want our clients to stay in business. Our ambition is for us to grow with our clients. It’s all about maintaining relationships. For that matter, we have created a couple events for the growth of the business community. We don’t have any intentions on making money off them. Below are the events we put together to help support our community.
</p><br>
         <img src="../journalnews/images/laptop-jacksonville.jpg" width="100%"><br><br>

<p><b> AFTER WORK NETWORKING MIXER</b><br>
One of the best ways to grow a business is by networking. By networking you can meet prospect clients and grow your connection. People always have a tendency to do business with people they know. A business can grow significantly just by getting out there and expand their connections. This event is a monthly event and free to attend. Our main principle about this event is that we do not sell to our guests. We give them a platform to grow their business and we are fine with that.
 </p><br>

<p><b>PROPEURBS WORKSHOP</b> <br>
PROPEURBS is a two-hour business workshop for creative entrepreneurs, small business owners and anyone looking to Spark Results. It’s a workshop where we teach aspiring and established entrepreneurs how to define their brand, master social media tools and fund their business. It’s also free to attend. We believe that knowledge worth more than money. So we want to create a literate business community and provide them the tools they need to be successful. 
</p><br>

<p><b>HIRE LOCAL</b><br> 
It’s a project focusing on encouraging local businesses to hire local talents and convincing people to shop local by sharing stories of small business owners and creative entrepreneurs living in Jacksonville, Florida. The project is free of charge and it gives local businesses a platform to make their voice heard and openly give their businesses more exposure.
</p><br>

<p>Antean Studios has more projects coming up to help grow the business community, such as a summit for the end of October and a soccer tournament before the end of the summer. These activities are free of charge and help unify our communities and make their members aware of the importance of local businesses. Whether we want to admit it or not, people do business with people they know. So we try to be proactive with our process. Instead of spending tones of money in advertisements, we do something to benefit our community while bringing awareness of our presence. It’s a win-win situation. 
 <br><br>

Now, if you are part of the Jacksonville business community, we are asking you to join us in unifying the business scene for the greater good of our business relationships and to sustain local productions. At last, we are for the Jacksonville business community.
