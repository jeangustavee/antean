        <p><span class="dropcap">I</span>t’s evident that we’ve all heard about Slack by now. It’s the new way to manage projects and keep your team alerted. Slack is a simple method to manage projects, but mainly communicating. Slack is an acronym for "Searchable Log of All Conversation and Knowledge." It’s a cloud-based set of team collaboration tools and services. 
</p><br>
        <p>Slack is very important to our company. By now, we don’t think our team would survive without Slack. We love Slack for many reasons. We use Slack from start to finish, from when we open our doors to close. We wanted to share some technical aspects with you about it whether you just started or are an expert, there’s always room to learn more from a fellow slacker.
</p><br>
         <img src="../journalnews/images/Desktop-App.png" width="100%"><br><br>

<p><b> Communication</b><br>
Communication is the number one feature of Slack. One of the main reasons why we use Slack at the office is to communicate with each other. This process is seemingly very convenient. You can communicate privately with other team members or just post something in channels for multiple team members to see at once. Company announcements and updates are seamless with Slack. It’s even more convenient because everyone in your team is using the mobile app. That way they can communicate and stay up-to-date on the go.

 </p><br>

<p><b>Project Management </b> <br>
Slack gives you the ability to create a post, assign it to a specific person (or channel), and keep track of the progress. This is super important at our office because we are working on multiple projects at once. Slack helps all team members identify who is working on what and where they are on that project. They can easily upload their progress or send a message about where they are at all in one place.

</p><br>

 <img src="../journalnews/images/Slack-chat.png" width="100%"><br><br>
<p><b>App Integration</b><br>  
What makes Slack one of the best platforms out there is that you can bring into Slack without stepping out on other apps that you love. You can use a variety of apps with Slack such as Dropbox, Google Drive, Asana, Trello, etc. Uploading files into Slack from Dropbox, for example, is very useful for us because we use Dropbox to work on projects with other team members simultaneously.

</p><br>
<img src="../journalnews/images/Slacl-Welcome-Desktop-App.png" width="100%"><br><br>
<p><b>Customized message in welcome loading screen</b><br>
Slack gives you a friendly feel in some aspects. It lets you customize the loading page. We definitely take great advantage of that. We set up announcements and reminders for the team that will pop up on the loading page. It’s a slick way to announce something because we all use Slack on a daily basis. Our team also finds it entertaining. How awesome is that when it’s your birthday to see your birthday announcement while loading an app? Slack does that.

 <br><br>

If you aren’t using Slack by now you should be! Slack is very easy to use and keeps you and your team connected with just one app! Using Slack at Antean Studios was one of best choices and it should be one of yours.
</p> 
